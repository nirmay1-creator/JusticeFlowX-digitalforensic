function initLedger() {
  ledgerList.innerHTML = '';
  custodyLogs.forEach(log => renderLog(log));
}

function addLog() {
  const evId = document.getElementById('evId').value || 'EVID-UNKNOWN';
  const caseId = document.getElementById('evCaseId').value || 'CAS-UNKNOWN';
  const name = document.getElementById('evName').value || 'Unnamed Evidence';
  const type = document.getElementById('evType').value;
  const size = document.getElementById('evSize').value || 'Unknown';
  const investigator = document.getElementById('evInvestigator').value || 'Unknown Agent';
  const device = document.getElementById('evDevice').value || 'Unknown Device';
  const srcIp = document.getElementById('evSrcIp').value || 'N/A';
  const dstIp = document.getElementById('evDstIp').value || 'N/A';
  const method = document.getElementById('evMethod').value;
  const location = document.getElementById('evLocation').value || 'Unknown Location';
  const notes = document.getElementById('evNotes').value || 'None';
  
  const now = new Date();
  const time = now.toISOString().replace('T', ' ').substring(0, 19);
  
  const newLog = {
    evId, caseId, name, type, size, investigator, device, srcIp, dstIp, method, location, notes,
    action: 'REGISTERED IN CUSTODY', time, hash: generateMockHash(),
    audit: { createdBy: 'SYSTEM', uploadedBy: investigator, integrity: 'VERIFIED' }
  };
  
  custodyLogs.push(newLog);
  renderLog(newLog);
}

function filterLedger() {
  const term = document.getElementById('searchAudit').value.toLowerCase();
  ledgerList.innerHTML = '';
  custodyLogs.filter(log => log.hash.toLowerCase().includes(term) || log.evId.toLowerCase().includes(term) || log.caseId.toLowerCase().includes(term)).forEach(log => renderLog(log));
}

