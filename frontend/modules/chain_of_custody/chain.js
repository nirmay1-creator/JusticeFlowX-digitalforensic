// Tech Stack Modal Logic
function showTechStack() {
  document.getElementById('techModal').classList.add('show');
}
function closeTechStack() {
  document.getElementById('techModal').classList.remove('show');
}
window.onclick = function(event) {
  if (event.target == document.getElementById('techModal')) {
    closeTechStack();
  }
}

// Export Logic
function exportItem(type) {
  const content = "SIMULATED EXPORT: " + type + "\n\n[Data dynamically generated by Flask-RESTful API in production...]";
  const blob = new Blob([content], { type: "text/plain" });
  const url = URL.createObjectURL(blob);
  const a = document.createElement("a");
  a.href = url;
  a.download = "export_" + type.replace(/ /g, "_").toLowerCase() + ".txt";
  document.body.appendChild(a);
  a.click();
  document.body.removeChild(a);
  URL.revokeObjectURL(url);
  alert("Exporting: " + type + "...");
}

// Function to generate a random mock SHA-256 hash
function generateMockHash() {
  const chars = '0123456789abcdef';
  let hash = '';
  for(let i=0; i<64; i++) {
    hash += chars[Math.floor(Math.random() * chars.length)];
  }
  return hash;
}

